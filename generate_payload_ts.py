    "class SEOAnalyzer:",
    "    def __init__(self, domain, timeout=10):",
    "        self.domain = domain.replace('https://', '').replace('http://', '').rstrip('/')",
    "        self.base_url = f'https://{self.domain}'",
    "        self.timeout = timeout",
    "",
    "    def fetch_page(self, url):",
    "        try:",
    "            req = urllib.request.Request(url, headers={'User-Agent': 'Mozilla/5.0 (SEOBot/1.0)'})",
    "            with urllib.request.urlopen(req, timeout=self.timeout) as r:",
    "                return r.read().decode('utf-8', errors='ignore'), dict(r.headers)",
    "        except Exception as e:",
    "            return '', {}",
    "",
    "    def analyze_on_page(self, url=None):",
    "        url = url or self.base_url",
    "        html, headers = self.fetch_page(url)",
    "        if not html: return {'error': 'Could not fetch page'}",
    "        issues = []",
    "        score = 100",
    "        title_match = re.search(r'<title[^>]*>(.*?)</title>', html, re.IGNORECASE | re.DOTALL)",
    "        title = title_match.group(1).strip() if title_match else ''",
    "        if not title: issues.append({'type': 'Missing title tag', 'severity': 'HIGH'}); score -= 20",
    "        elif len(title) < 30: issues.append({'type': f'Title too short ({len(title)} chars)', 'severity': 'MEDIUM'}); score -= 5",
    "        elif len(title) > 60: issues.append({'type': f'Title too long ({len(title)} chars)', 'severity': 'MEDIUM'}); score -= 5",
    "        desc_match = re.search(r'<meta[^>]+name=[\"\\']description[\"\\'][^>]+content=[\"\\']([^\"\\']*)[\"\\']', html, re.IGNORECASE)",
    "        desc = desc_match.group(1) if desc_match else ''",
    "        if not desc: issues.append({'type': 'Missing meta description', 'severity': 'HIGH'}); score -= 15",
    "        h1_tags = re.findall(r'<h1[^>]*>(.*?)</h1>', html, re.IGNORECASE | re.DOTALL)",
    "        if not h1_tags: issues.append({'type': 'Missing H1 tag', 'severity': 'HIGH'}); score -= 15",
    "        img_tags = re.findall(r'<img[^>]*>', html, re.IGNORECASE)",
    "        imgs_no_alt = [t for t in img_tags if 'alt=' not in t.lower()]",
    "        if imgs_no_alt: issues.append({'type': f'{len(imgs_no_alt)} images missing alt text', 'severity': 'MEDIUM'}); score -= min(10, len(imgs_no_alt))",
    "        return {'url': url, 'score': max(0, score), 'title': title, 'meta_description': desc, 'h1_count': len(h1_tags), 'issues': issues, 'images_total': len(img_tags), 'images_missing_alt': len(imgs_no_alt)}",
    "",
    "    def keyword_density(self, keywords, url=None):",
    "        url = url or self.base_url",
    "        html, _ = self.fetch_page(url)",
    "        text = re.sub(r'<[^>]+>', ' ', html).lower()",
    "        word_count = len(text.split())",
    "        results = {}",
    "        for kw in keywords:",
    "            count = text.count(kw.lower())",
    "            density = (count / word_count * 100) if word_count else 0",
    "            results[kw] = {'count': count, 'density': round(density, 2), 'word_count': word_count}",
